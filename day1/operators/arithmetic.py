class ArithmeticOperations:
    def add(self, a, b):
        return a + b
    
    def sub(self, a, b):
        return a - b
    
    def divide(self, a, b):
        return a / b
    
    def product(self, a, b):
        return a * b
    
    def floor_division(self, a, b):
        return a // b
    
    def exponent(self, a, b):
        return a ** b
    
    def remainder(self, a, b):
        return a % b


